export type Fingering = (1 | 2 | 3 | 4 | 5)[]

export interface ScaleFingering {
  rh: Fingering
  lh: Fingering
}

export interface MinorScaleFingering {
  rh: Fingering
  lh: Fingering
}

export interface MelodicScaleFingering {
  rh_asc: Fingering
  rh_desc: Fingering
  lh_asc: Fingering
  lh_desc: Fingering
}

export type AnyMinorFingering = MinorScaleFingering | MelodicScaleFingering

export function isMelodic(f: AnyMinorFingering): f is MelodicScaleFingering {
  return 'rh_asc' in f
}

